/// قوالب الـ prompts للـ AI agent. كلها تطلب JSON منظّم عشان نحلّله بأمان.
/// مكتوبة بعناية للأكل الخليجي + تنويه "تقديري" إلزامي.
abstract class AiPrompts {
  /// نظام عام للمساعد — يحدّد الشخصية والحواجز.
  static const system = '''
أنت "رفيق"، مساعد تغذية ولياقة عربي ذكي داخل تطبيق زاد.
- تحدّث بعربية واضحة ودودة (لهجة سعودية بيضاء مفهومة).
- خبير بالأكل الخليجي والسعودي (كبسة، مندي، مظبي، شاورما، فطائر...).
- قيمك تقديرية دائماً — نبّه أن الأرقام تقريبية وليست دقة طبية.
حواجز أمان إلزامية:
- لا تعطِ تشخيصاً أو نصيحة طبية. للحالات المرضية أو الحمل: وجّه لمختص.
- لا تشجّع على سعرات خطيرة منخفضة (أقل من 1200 سعرة) أو سلوك إيذاء.
- إذا ظهرت علامات اضطراب أكل، توقّف ووجّه بلطف لطلب مساعدة مختص.
''';

  /// تقدير سعرات صنف من وصف نصّي. يرجّع JSON فقط.
  static String estimateFromText(String description) => '''
قدّر القيم الغذائية الواقعية لهذا الطعام بناءً على معرفتك، كما يُقدّم عادةً في مطعم سعودي
(الطبق كامل بكل مكوّناته — مثلاً الكبسة = أرز + دجاج + مكوّنات، وليست مكوّناً واحداً).

قواعد صارمة:
- أعطِ أرقاماً واقعية فعلية. ممنوع منعاً باتاً إرجاع أصفار.
- اكتب الحقول النصية بالعربية.
- إن لم تتعرّف على الطبق بدقة اجعل "confidence" = "low".

أرجِع JSON فقط بهذا الشكل (استبدل الأرقام بقيم حقيقية مقدّرة، هذا مثال فقط):
{"name":"كبسة دجاج","calories":750,"protein_g":35,"carbs_g":90,"fat_g":25,"confidence":"medium","note":"تقديري"}

الطعام: "$description"
''';

  /// تقدير سعرات من صورة طعام (للمزوّدات اللي تدعم الرؤية).
  static String estimateFromImage() => '''
حلّل صورة الطعام وقدّر قيمها الغذائية الواقعية. الكمية مقدّرة من الصورة.
قواعد صارمة: أعطِ أرقاماً واقعية فعلية (ممنوع أصفار)، والحقول النصية بالعربية.
أرجِع JSON فقط بهذا الشكل (الأرقام مثال — استبدلها بتقديرك):
{"name":"اسم الطبق بالعربية","calories":600,"protein_g":30,"carbs_g":70,"fat_g":20,"confidence":"medium","note":"تقديري من الصورة"}
''';

  /// تسجيل عدّة أصناف من جملة محادثة. يرجّع مصفوفة JSON.
  static String parseMealMessage(String message) => '''
$system

المستخدم وصف ما أكله بجملة. استخرج كل صنف وقدّر قيمه.
أرجِع JSON فقط:
{
  "items": [
    {"name": "...", "calories": 0, "protein_g": 0, "carbs_g": 0, "fat_g": 0}
  ],
  "reply": "رد ودود قصير يلخّص ما سُجّل"
}

الرسالة: "$message"
''';

  /// اقتراح وجبة من مطعم قريب تناسب السعرات المتبقية.
  static String suggestMeal({
    required String restaurant,
    required int remainingCalories,
    required int remainingProtein,
  }) => '''
$system

المستخدم قريب من مطعم "$restaurant".
المتبقّي له اليوم: $remainingCalories سعرة، و$remainingProtein جرام بروتين.
اقترح وجبة واحدة مناسبة من نوع هذا المطعم تقريباً ضمن المتبقّي.
أرجِع JSON فقط:
{
  "meal": "اسم الوجبة المقترحة",
  "calories": رقم صحيح,
  "protein_g": رقم صحيح,
  "reason": "سبب قصير لماذا تناسبه",
  "disclaimer": "تنويه أن القيم تقديرية ومن بيانات السلسلة"
}
''';
}
